/**
 * ערכי גימטריה לכל האותיות העבריות כולל סופיות
 * כ=ך=20, מ=ם=40, נ=ן=50, פ=ף=80, צ=ץ=90
 */
export const GEMATRIA: Readonly<Record<string, number>> = {
  'א': 1,  'ב': 2,  'ג': 3,  'ד': 4,  'ה': 5,
  'ו': 6,  'ז': 7,  'ח': 8,  'ט': 9,  'י': 10,
  'כ': 20, 'ך': 20, 'ל': 30, 'מ': 40, 'ם': 40,
  'נ': 50, 'ן': 50, 'ס': 60, 'ע': 70, 'פ': 80,
  'ף': 80, 'צ': 90, 'ץ': 90, 'ק': 100, 'ר': 200,
  'ש': 300, 'ת': 400,
} as const;

/**
 * אותיות גרון — תנועות בשפה העברית שמשמשות לחישוב מספר הנשמה
 * (האנרגיה הפנימית של האדם — מה שמניע אותו מבפנים)
 */
export const HEBREW_VOWELS: readonly string[] = ['א', 'ה', 'ו', 'י', 'ע'] as const;

/**
 * מנקה טקסט עברי מניקוד, פיסוק ורווחים לפני חישוב גימטריה
 *
 * מדוע: חישוב גימטריה מדויק דורש אותיות בלבד — ניקוד ורווחים יגרמו לתוצאות שגויות
 *
 * @param text - הטקסט העברי לניקוי
 * @returns הטקסט המנוקה — אותיות עבריות בלבד
 */
export function cleanHebrewText(text: string): string {
  if (!text) return '';
  return text
    .replace(/[\u0591-\u05C7]/g, '') // הסרת ניקוד וטעמי מקרא
    .replace(/[״׳־]/g, '')           // הסרת פיסוק עברי (גרשיים, מקף)
    .replace(/\s+/g, '')              // הסרת כל הרווחים
    .trim();
}

/**
 * מחשב את ערך הגימטריה הכולל של טקסט עברי
 *
 * מדוע: ערך הגימטריה הכולל (לפני צמצום) נדרש לחישובי מספר הגורל והנשמה
 *
 * @param text - הטקסט העברי (מנוקה כבר, ללא ניקוד)
 * @returns סכום ערכי הגימטריה של כל אות
 */
export function calculateGematria(text: string): number {
  let sum = 0;
  for (const char of text) {
    sum += GEMATRIA[char] ?? 0;
  }
  return sum;
}
