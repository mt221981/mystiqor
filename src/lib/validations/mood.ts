/**
 * סכמות Zod למצב רוח — ולידציה לרישום יומי של מצב רוח ואנרגיה
 * תואם לטבלת mood_entries ב-Supabase
 */

import { z } from 'zod';

/** סכמת יצירת רשומת מצב רוח */
export const MoodCreateSchema = z.object({
  /** תיאור מצב הרוח — חובה */
  mood: z.string().min(1, 'יש לבחור מצב רוח'),

  /** ציון מצב רוח — בין 1 ל-10 */
  mood_score: z
    .number()
    .int('ציון מצב רוח חייב להיות מספר שלם')
    .min(1, 'ציון חייב להיות בין 1-10')
    .max(10, 'ציון חייב להיות בין 1-10'),

  /** רמת אנרגיה — אופציונלי, בין 1 ל-10 */
  energy_level: z
    .number()
    .int('רמת אנרגיה חייבת להיות מספר שלם')
    .min(1, 'רמת אנרגיה חייבת להיות בין 1-10')
    .max(10, 'רמת אנרגיה חייבת להיות בין 1-10')
    .optional(),

  /** רמת לחץ — אופציונלי, בין 1 ל-10 */
  stress_level: z
    .number()
    .int('רמת לחץ חייבת להיות מספר שלם')
    .min(1, 'רמת לחץ חייבת להיות בין 1-10')
    .max(10, 'רמת לחץ חייבת להיות בין 1-10')
    .optional(),

  /** איכות שינה — אופציונלי, בין 1 ל-10 */
  sleep_quality: z
    .number()
    .int('איכות שינה חייבת להיות מספר שלם')
    .min(1, 'איכות שינה חייבת להיות בין 1-10')
    .max(10, 'איכות שינה חייבת להיות בין 1-10')
    .optional(),

  /** הערות חופשיות — אופציונלי */
  notes: z.string().max(500, 'הערות לא יכולות לעלות על 500 תווים').optional(),

  /** פעילויות שבוצעו היום — ברירת מחדל רשימה ריקה */
  activities: z.array(z.string()).default([]),

  /** פריטי הכרת תודה — ברירת מחדל רשימה ריקה */
  gratitude: z.array(z.string()).default([]),
});

/** טיפוס נתוני יצירת מצב רוח */
export type MoodCreate = z.infer<typeof MoodCreateSchema>;
